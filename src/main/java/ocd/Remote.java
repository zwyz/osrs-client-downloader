package ocd;

public class Remote {
    private String baseUrl;
    private String deltaFormat;
    private String flags;
    private String pieceFormat;
    private String type;

    public String getBaseUrl() {
        return baseUrl;
    }

    public String getDeltaFormat() {
        return deltaFormat;
    }

    public String getFlags() {
        return flags;
    }

    public String getPieceFormat() {
        return pieceFormat;
    }

    public String getType() {
        return type;
    }
}
